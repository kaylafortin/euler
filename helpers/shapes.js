export const getTriangleValue = (n) => ((1 / 2) * (n)) * (n + 1)

export const getPentagonalValue = (n) => (n * (3 * n - 1)) / 2

export const getHexagonalValue = (n) => n * (2 * n - 1)

export const getSquareValue = (n) => n ** 2;

export const getHeptagonalValue = (n) => (n * (5 * n - 3)) / 2;

export const getOctagonalValue = (n) => n * (3 * n - 2);